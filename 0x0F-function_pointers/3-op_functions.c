/**
 * op_add- adds numbers
 * @a: num1
 * @b: num2
 *
 * Description- sum of two nums
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- subtract numbers
 * @a: num1
 * @b: num2
 *
 * Description - sub of two nums
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- multiply numbers
 * @a: num1
 * @b: num2
 *
 * Description - multiplly of two nums
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- divide numbers
 * @a: num1
 * @b: num2
 *
 * Description - divide of two nums
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modul numbers
 * @a: num1
 * @b: num2
 *
 * Description - find module of two nums
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
